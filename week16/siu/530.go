/**
 * Definition for a binary tree node.
 * type TreeNode struct {
 *     Val int
 *     Left *TreeNode
 *     Right *TreeNode
 * }
 */

var pre int
var res int

func getMinimumDifference(root *TreeNode) int {
	pre = -1
	res = math.MaxInt64
	dfs530(root)
	return res
}

func dfs530(node *TreeNode) {
	if node == nil {
		return
	}

	dfs530(node.Left)
	if pre != -1 && node.Val-pre < res {
		res = node.Val - pre
	}
	pre = node.Val
	dfs530(node.Right)
}

